# valor é determinado pelo usuário

num1 = int(input('Insira um número para mostrar o antecessor e o sucessor: '))

# determina o antecessor e sucessor fazendo a forma básica

antecessor = num1 - 1
sucessor = num1 + 1

# printa a resposta

print(f'o antecessor de {num1} é {antecessor} e o sucessor é {sucessor}')