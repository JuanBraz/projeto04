# os valores são determinados pelo usuário

altura = int(input('Insira a altura do retangulo: '))
largura = int(input('Insira a largura do retangulo: '))

# determina o perimetro pela forma basica da adição dos lados

perimetro = (altura + largura) * 2

# printa a respostas

print('O valor do perímetro é: ', perimetro)